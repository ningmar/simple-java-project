    private void jButton_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_addActionPerformed
        // TODO add your handling code here:
        String stime = jTextField_starttime.getText();
        String etime = jTextField_endtime.getText();
        String activity = jTextArea_activity.getText();
        String reference = jTextField_reference.getText();
        
        routine ob = new routine();
        ob.setStarttime(stime);
        ob.setEndtime(etime);
        ob.setActivity(activity);
        ob.setReference(reference);
        routineDAO rDAO = new routineDAO();//hand over this model class to controller class
        rDAO.insertData(ob);
        JOptionPane.showMessageDialog(null,"An Activity Added.");
        //clear all the fields
        clearfields();
        LoadData();
    }//GEN-LAST:event_jButton_addActionPerformed

    private void jButton_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_deleteActionPerformed
        // TODO add your handling code here:
        int id =Integer.parseInt(jLabel_id.getText());
        routine ab = new routine();
        ab.setId(id);
        routineDAO ob = new routineDAO();
        int deleteoption = JOptionPane.showConfirmDialog(null,"Do you want to delete?");
        if(deleteoption == JOptionPane.YES_OPTION) {
        ob.deleteData(ab);
        JOptionPane.showMessageDialog(null,"An Activity Deleted");
        LoadData();
        }
    }//GEN-LAST:event_jButton_deleteActionPerformed

    private void jButton_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_editActionPerformed
        // TODO add your handling code here:
        int id =Integer.parseInt(jLabel_id.getText());
        String stime = jTextField_starttime.getText();
        String etime = jTextField_endtime.getText();
        String activity = jTextArea_activity.getText();  
        routine ob = new routine();
        ob.setId(id);
        ob.setStarttime(stime);
        ob.setEndtime(etime);
        ob.setActivity(activity);
        routineDAO evntDAO = new routineDAO();//hand over this model class to controller class
        evntDAO.updateData(ob);
        //clear all the fields
        JOptionPane.showMessageDialog(null,"Selected Activity Edited");
        LoadData(); 
    }//GEN-LAST:event_jButton_editActionPerformed

