    private void clearfields(){
        jTextField_reference.setText("0");
        jTextField_reference.setEditable(false);
        jTextField_starttime.setText("00:00");
        jTextField_endtime.setText("00:00");
        jTextArea_activity.setText("");
    }
    
    private void clearTable()
    {
        int count = jTable_routine.getRowCount();
        for(int i=0; i<count;i++)
        {
            model.removeRow(0);
        }
    }
    
    private void LoadData(){
        routineDAO rDAO = new routineDAO();
        ArrayList<routine> list = rDAO.fetchData();
        clearTable();
        for (int i = 0; i<list.size(); i++)
        {
                model.addRow(new Object[]{
                list.get(i).getId(),
                list.get(i).getStarttime(),
                list.get(i).getActivity(),
                list.get(i).getEndtime()                    
            });
        }
    }
